function setLanguage(lang) {

  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach(element => {

    const keys = element.dataset.i18n.split(".");

    let value = translations[lang];

    keys.forEach(key => {
      value = value?.[key];
    });

    if (value !== undefined) {
      element.innerHTML = value;
    }

  });

  document.title =
    lang === "es"
      ? "Brasaland — Restaurante en Colombia y Florida"
      : "Brasaland — Grill Restaurant in Colombia and Florida";

  document
    .querySelector('meta[name="description"]')
    .setAttribute(
      "content",
      lang === "es"
        ? "Brasaland: cocina de temporada en Colombia y Florida con el estándar de servicio de la hostelería de hotel boutique."
        : "Brasaland: seasonal cuisine in Colombia and Florida with boutique hotel hospitality."
    );

}

document.getElementById("lang-es").addEventListener("click", () => setLanguage("es"));
document.getElementById("lang-en").addEventListener("click", () => setLanguage("en"));

